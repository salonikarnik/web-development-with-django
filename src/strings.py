name = 'Tim'

print('Hi. \n\'How are you\'')
print(name)

#Get first letter
print(name[0])

#Convert to upper case
print(name.upper())

#Convert to lower case
print(name.lower())

#Check if its lowercase
print(name.islower())

print(name.lower().islower())

#Length of a string
print(len(name))

#Index of letter I
print(name.index('i'))

#Replace a letter
print(name.replace('m', 'n'))
